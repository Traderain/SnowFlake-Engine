namespace Math3D
{
    public class Vector3I
    {
        public int X;
        public int Y;
        public int Z;

        public Vector3I()
        {
            X = 0;
            Y = 0;
            Z = 0;
        }

        public Vector3I(int x, int y, int z)
        {
            X = 0;
            Y = 0;
            Z = 0;
            X = x;
            Y = y;
            Z = z;
        }
    }
}