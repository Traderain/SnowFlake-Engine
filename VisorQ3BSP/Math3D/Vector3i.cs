using System;

namespace Math3D
{
    public class Vector3i
    {
        public int X;
        public int Y;
        public int Z;

        public Vector3i()
        {
            this.X = 0;
            this.Y = 0;
            this.Z = 0;
        }

        public Vector3i(int X, int Y, int Z)
        {
            this.X = 0;
            this.Y = 0;
            this.Z = 0;
            this.X = X;
            this.Y = Y;
            this.Z = Z;
        }
    }
}

